import pygame

def init(son_menu):
    """Cette fonction renvoie tout les éléments du jeu qui ne changent pas et qui ont besoin d'être réinitialisés au début de chaque partie."""
    voiture = pygame.Rect(635, 350, 30, 60)
    voiture.x == 635
    voiture.y == 350
    son_menu.play()
    return voiture
